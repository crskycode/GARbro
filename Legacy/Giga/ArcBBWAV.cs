        public override ArcFile TryOpen(ArcView file) {
            var base_name = Path.GetFileName(file.Name).ToLower();
            if (base_name != "bbwav.dat")
                return null;
            var dir = new List<Entry>(EntrySizeMap.Count);
            uint offset = 0;
            foreach (var kv in EntrySizeMap) {
                uint size = kv.Value * 1024u;
                var entry = new Entry {
                    Name = $"{kv.Key}.wav",
                    Offset = offset,
                    Size = size,
                    Type = "audio"
                };
                offset += size;
                dir.Add(entry);
            }
            return new ArcFile(file, this, dir);
        }

